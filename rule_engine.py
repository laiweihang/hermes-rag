"""规则引擎模块。

定位：在 RAG 流程之前作为"零延迟直答层"，对高频常识类问题（劳动法、
年假、试用期等）通过关键词或正则命中预设答案，避免触发 LLM 调用。

调用方：rag_engine.generate_answer / generate_answer_stream 在收到用户
问题后第一步即调用 ``check_rules``；命中则直接返回模板答案，否则继续走
向量检索 + LLM 生成路径。

设计取舍：
- 规则配置直接以 Python 列表 ``RULES`` 内联在源码中。条目极少（个位数）
  且修改频率低，没必要单独引入 yaml/db；如需扩展，可改为从数据库
  ``Skill`` 表加载（参见 ``models.Skill.detect_keywords``）。
- 关键词匹配优先于正则，因为关键词更直观，命中率高且开销几乎为零。
"""

import re

# 规则库：每条规则包含
# - keywords: 任一字符串作为子串出现在问题中即命中
# - pattern: 关键词未命中时再尝试的正则（处理"加班的工资怎么算"这类
#            语序变化）
# - answer:  命中后直接返回的固定答案文本
RULES = [
    {
        "keywords": ["加班费", "加班工资"],
        "pattern": r"加班.*?(?:工资|费)",
        "answer": "根据《劳动法》第四十四条，安排劳动者延长工作时间的，支付不低于工资的150%的工资报酬；休息日安排工作又不能补休的，支付不低于200%的工资报酬；法定休假日安排工作的，支付不低于300%的工资报酬。"
    },
    {
        "keywords": ["年假", "带薪年休假"],
        "pattern": r"(?:带薪)?年?假",
        "answer": "根据《职工带薪年休假条例》，职工累计工作已满1年不满10年的，年休假5天；已满10年不满20年的，年休假10天；已满20年的，年休假15天。"
    },
    {
        "keywords": ["试用期", "试用"],
        "pattern": r"试用",
        "answer": "劳动合同期限三个月以上不满一年的，试用期不得超过一个月；一年以上不满三年的，试用期不得超过二个月；三年以上固定期限和无固定期限的劳动合同，试用期不得超过六个月。"
    }
]


def check_rules(question: str):
    """检查用户问题是否命中预置规则。

    Args:
        question: 用户原始提问，建议未做任何归一化（保留中文标点等）。

    Returns:
        命中时返回该规则的 ``answer`` 字符串；未命中返回 ``None``，由调用
        方继续走 RAG / LLM 流程。

    匹配顺序：先关键词子串、再正则。任一规则命中即立即返回，不再尝试
    后续规则（先到先得，因此 ``RULES`` 中的顺序具有优先级语义）。
    """
    for rule in RULES:
        # 关键词子串匹配：开销最低，覆盖大多数自然提问。
        if any(keyword in question for keyword in rule["keywords"]):
            return rule["answer"]
        # 关键词漏匹配时再做正则兜底，应对"加班的工资"这类被字符
        # 隔开的表述。
        if re.search(rule["pattern"], question):
            return rule["answer"]
    return None
