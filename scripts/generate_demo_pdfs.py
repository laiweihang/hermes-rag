"""
生成演示用文本型 PDF（不依赖 OCR），供文档管理页上传与 RAG 演示。

用法:
  python scripts/generate_demo_pdfs.py
  python scripts/generate_demo_pdfs.py --out ./demo/pdf
  DEMO_FONT_PATH=/path/to/NotoSansSC-Regular.otf python scripts/generate_demo_pdfs.py

若自动探测不到中文字体，请设置环境变量 DEMO_FONT_PATH 或传入 --font。
"""
from __future__ import annotations

import argparse
import os
import platform


def _candidate_font_paths() -> list[str]:
    extra = os.environ.get("DEMO_FONT_PATH", "").strip()
    paths: list[str] = []
    if extra:
        paths.append(extra)
    system = platform.system()
    if system == "Darwin":
        paths.extend([
            "/System/Library/Fonts/STHeiti Light.ttc",
            "/System/Library/Fonts/PingFang.ttc",
            "/System/Library/Fonts/Hiragino Sans GB.ttc",
            "/Library/Fonts/Arial Unicode.ttf",
        ])
    elif system == "Linux":
        paths.extend([
            "/usr/share/fonts/opentype/noto/NotoSansCJK-Regular.ttc",
            "/usr/share/fonts/truetype/noto/NotoSansCJK-Regular.ttc",
            "/usr/share/fonts/truetype/wqy/wqy-microhei.ttc",
            "/usr/share/fonts/truetype/arphic/uming.ttc",
        ])
    elif system == "Windows":
        windir = os.environ.get("WINDIR", "C:\\Windows")
        paths.extend([
            os.path.join(windir, "Fonts", "msyh.ttc"),
            os.path.join(windir, "Fonts", "simhei.ttf"),
            os.path.join(windir, "Fonts", "simsun.ttc"),
        ])
    here = os.path.dirname(os.path.abspath(__file__))
    project_root = os.path.dirname(here)
    paths.append(os.path.join(project_root, "demo", "fonts", "NotoSansSC-Regular.otf"))
    return paths


def _resolve_font_path(explicit: str | None) -> str:
    if explicit and os.path.isfile(explicit):
        return explicit
    for p in _candidate_font_paths():
        if p and os.path.isfile(p):
            return p
    raise SystemExit(
        "未找到可用的中文字体文件。请安装 Noto CJK 字体到 demo/fonts/NotoSansSC-Regular.otf，"
        "或设置 DEMO_FONT_PATH / 使用 --font 指定 .ttf/.ttc 路径。"
    )


def _write_pdf(path: str, title: str, body: str, fontfile: str) -> None:
    """写入多页中文 PDF。自动处理分页。"""
    import fitz

    W, H = 595, 842  # A4
    MARGIN = 56
    TITLE_SIZE = 14
    BODY_SIZE = 10.5
    LINE_HEIGHT = BODY_SIZE * 1.6

    doc = fitz.open()
    text_rect = fitz.Rect(MARGIN, MARGIN, W - MARGIN, H - MARGIN)

    page = doc.new_page(width=W, height=H)
    page.insert_font(fontname="zh", fontfile=fontfile)

    title_rect = fitz.Rect(MARGIN, MARGIN, W - MARGIN, MARGIN + TITLE_SIZE * 2.5)
    page.insert_textbox(title_rect, title, fontname="zh", fontsize=TITLE_SIZE,
                        align=fitz.TEXT_ALIGN_CENTER)

    body_top = MARGIN + TITLE_SIZE * 3.2
    paragraphs = body.split("\n")
    y = body_top

    for para in paragraphs:
        para = para.strip()
        if not para:
            y += LINE_HEIGHT * 0.6
            if y > H - MARGIN:
                page = doc.new_page(width=W, height=H)
                page.insert_font(fontname="zh", fontfile=fontfile)
                y = MARGIN
            continue

        usable_w = W - 2 * MARGIN
        chars_per_line = int(usable_w / (BODY_SIZE * 0.55))
        if chars_per_line < 10:
            chars_per_line = 10
        lines_needed = max(1, -(-len(para) // chars_per_line))
        block_h = lines_needed * LINE_HEIGHT + 2

        if y + block_h > H - MARGIN:
            page = doc.new_page(width=W, height=H)
            page.insert_font(fontname="zh", fontfile=fontfile)
            y = MARGIN

        rect = fitz.Rect(MARGIN, y, W - MARGIN, y + block_h + LINE_HEIGHT)
        page.insert_textbox(rect, para, fontname="zh", fontsize=BODY_SIZE,
                            align=fitz.TEXT_ALIGN_LEFT)
        y += block_h

    os.makedirs(os.path.dirname(path) or ".", exist_ok=True)
    doc.save(path)
    doc.close()


