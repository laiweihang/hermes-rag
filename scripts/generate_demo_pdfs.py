def main() -> None:
    parser = argparse.ArgumentParser(description="生成演示用 PDF 到 demo/pdf/")
    parser.add_argument(
        "--out",
        default=os.path.join(os.path.dirname(os.path.dirname(__file__)), "demo", "pdf"),
        help="输出目录（默认项目根目录下 demo/pdf）",
    )
    parser.add_argument("--font", default=None, help="中文字体文件路径（.ttf / .ttc）")
    args = parser.parse_args()
    out_dir = os.path.abspath(args.out)
    font_path = _resolve_font_path(args.font)
    print(f"使用字体: {font_path}")
    os.makedirs(out_dir, exist_ok=True)

    specs: list[tuple[str, str, str]] = [
        ("01_员工手册-年假试用期与加班说明.pdf",
         "某某科技有限公司员工手册（2025 年修订版）", HANDBOOK),
        ("02_2025Q1公司利润表摘要.pdf",
         "2025 年第一季度利润表摘要（合并报表）", FINANCE),
        ("03_部门周例会纪要-20250401.pdf",
         "部门周例会纪要 — 2025-04-01", MEETING),
        ("04_报销用-增值税普通发票信息摘录.pdf",
         "2025 年 3 月份报销发票信息汇总表", INVOICE),
    ]

    for filename, title, body in specs:
        full = os.path.join(out_dir, filename)
        _write_pdf(full, title, body, font_path)
        size_kb = os.path.getsize(full) / 1024
        print(f"已写入: {full}  ({size_kb:.0f} KB)")

    print(f"\n完成，共 {len(specs)} 个文件，目录: {out_dir}")


if __name__ == "__main__":
    main()
